class ArrayCalculator {
    public static int sumOfArray(int[] arr) {
        int tong = 0;
        for (i=0; i<arr.length; i++) {
            tong += arr[i];
        }
        return tong;
    }

    public static double sumOfArray(double[] arr) {
        double tong = 0;
        for (i=0; i<arr.length; i++) {
            tong += arr[i];
        }
        return tong;
    }
}
